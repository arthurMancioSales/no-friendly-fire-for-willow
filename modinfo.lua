Rebalanceia totalmente as penalidades de fogo da Willow para que aliados não sofram efeitos negativos ao combater ao lado dela.

Funcionalidades incluídas:
- Aliados NÃO sofrem overheating causado por nenhuma chama ou skill da Willow (lunar, shadow, combustion, ignite, etc).
- Aliados NÃO sofrem penalidades de proximidade como freezing damage ou burn-ticks gerados pelas skills da Willow.
- Aliados NÃO recebem dano ao atacar inimigos queimando por fogo/skills da Willow.
- Itens dropados NÃO entram em smoldering ao serem expostos ao calor gerado pelas habilidades da Willow.
- O fogo originado das skills da Willow NÃO se espalha para o ambiente (árvores, flores, colmeias, estruturas, etc).
- Ataques e skills da Willow passam a marcar corretamente as fontes de fogo, garantindo compatibilidade com todos os módulos do mod.

Tudo é configurável individualmente, mas vem totalmente ativado por padrão.
