description = [[
== ENGLISH ==
Completely rebalances Willow's fire penalties so allies don't suffer negative effects when fighting alongside her.

Features included:
- Allies DO NOT suffer overheating from any Willow flame or skill (lunar, shadow, combustion, ignite, etc).
- Allies DO NOT suffer proximity penalties like freezing damage or burn-ticks from Willow's skills.
- Allies DO NOT take damage when attacking enemies burning from Willow's fire/skills.
- Dropped items DO NOT smolder when exposed to heat from Willow's abilities.
- Fire from Willow's skills DOES NOT spread to the environment (trees, flowers, beehives, structures, etc).
- Willow's attacks and skills properly tag fire sources, ensuring compatibility across all mod modules.

Everything is individually configurable, but all features are enabled by default.

